        /// <summary>
        /// ＥＸＣＥＬのブックオブジェクトにデータを設定する
        /// </summary>
        /// <remarks>
        /// 
        /// </remarks>
        /// <param name="input">入力</param>
        /// <param name="book">ＥＸＣＥＬのブックオブジェクト</param>
        /// <returns>戻り値</returns>
        /// <history>
        /// 日付　　　　担当者　　　内容
        /// 2014/12/05　AnhNV　　    新規作成
        /// </history>
        ////////////////////////////////////////////////////////////////////////////
        protected override IPrint007DaichoKingunsuBLOutput SetBook(IPrint007DaichoKingunsuBLInput input, Excel.Workbook book)
        {
            IPrint007DaichoKingunsuBLOutput output = new Print007DaichoKingunsuBLOutput();

            Excel.Application application = null;
            Excel.Worksheet outputSheet = null;

            try
            {
                application = book.Application;

                // Prevents from display messages of Excel
                application.DisplayAlerts = false;

                // Output sheet
                outputSheet = (Excel.Worksheet)book.Sheets[1];
                outputSheet.Name = "野帳";

                // Current processing row
                int curRow = 4;

                foreach (KensaDaicho11joHdrTblDataSet.YachoOutputKensaListRow row in input.PrintTable.Select(string.Empty, "SuishitsuKensaIraiNoCol asc"))
                {
                    // Header - 1 time only
                    if (curRow == 4)
                    {
                        // Get nendo
                        string nendo = Utility.DateUtility.GetNendo(input.SystemDt).ToString();
                        nendo = Utility.DateUtility.ConvertToWareki(nendo, "gyy年度", Utility.DateUtility.GengoKbn.Wareki);

                        // Header
                        CellOutput(outputSheet, 0, 11, nendo);
                    }

                    // (2)
                    CellOutput(outputSheet, curRow, 1, "'" + row.SuishitsuKensaIraiNoCol);
                    // (3)
                    CellOutput(outputSheet, curRow + 1, 1, "'" + row.SetchishaNmCol);

                    curRow += 3;
                }
            }
            catch
            {
                throw;
            }
            finally
            {
                #region オブジェクトを解放
                if (application != null) { Marshal.ReleaseComObject(application); }
                if (outputSheet != null) { Marshal.ReleaseComObject(outputSheet); }
                #endregion
            }

            return output;
        }
        #endregion

        #endregion
    }
