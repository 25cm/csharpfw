        #region Execute
        ////////////////////////////////////////////////////////////////////////////
        //  メソッド名 ： Execute
        /// <summary>
        /// 
        /// </summary>
        /// <param name="input"></param>
        /// <returns></returns>
        /// <history>
        /// 日付　　　　担当者　　　内容
        /// 2014/06/20  DatNT　　    新規作成
        /// </history>
        ////////////////////////////////////////////////////////////////////////////
        public override IDeleteBtnClickALOutput Execute(IDeleteBtnClickALInput input)
        {
            TraceLog.StartWrite(MethodInfo.GetCurrentMethod());

            // 出力クラスの実体作成
            IDeleteBtnClickALOutput output = new DeleteBtnClickALOutput();

            try
            {
                StartTransaction();

                IGetHokenjoMstByKeyBLInput getHokenjoBlInput    = new GetHokenjoMstByKeyBLInput();
                getHokenjoBlInput.HokenjoCd                     = input.HokenjoCd;
                IGetHokenjoMstByKeyBLOutput getHokenjoBlOutput  = new GetHokenjoMstByKeyBusinessLogic().Execute(getHokenjoBlInput);

                if (getHokenjoBlOutput.HokenjoMstDT != null && getHokenjoBlOutput.HokenjoMstDT.Count > 0)
                {
                    IDeleteHokenjoMstByKeyBLInput deleteInput   = new DeleteHokenjoMstByKeyBLInput();
                    deleteInput.HokenjoCd                       = input.HokenjoCd;
                    IDeleteHokenjoMstByKeyBLOutput deleteOutput = new DeleteHokenjoMstByKeyBusinessLogic().Execute(deleteInput);

                    output.Result = true;
                }
                else
                {
                    output.Result = false;
                }

                CompleteTransaction();
            }
            catch
            {
                throw;
            }
            finally
            {
                EndTransaction();

                TraceLog.EndWrite(MethodInfo.GetCurrentMethod());
            }

            return output;
        }
        #endregion

        #endregion
    }
